# --- Brain-inspired relation formation (Hebbian co-occurrence + homeostatic prune) ---
# Replaces the old "random concept pair + shared-token => verified relation" worker,
# which manufactured noise ("Summer" => "Its summer home" ↔ "Something Like Summer").
#
# How the brain avoids/handles such noise (and what we imitate):
#   1. Hebbian: a link forms only when two concepts ACTUALLY co-occur in the same real
#      sentence (fire together → wire together) — not random pairing, not name-substring.
#   2. Reinforcement: one co-occurrence is noise; a relation is only "linked" after it
#      recurs across >= K distinct sentences (repetition = signal).
#   3. Homeostasis (sleep down-scaling / SHY): every cycle all weights decay; pairs that
#      fall below 1.0 are pruned. One-off coincidences fade; repeated relations survive.
#      This also BOUNDS memory (the cooc table is capped) — critical for trillion-scale.
_RELDISC_LINK_THRESHOLD = 3        # K reinforcements before a pair becomes a relation
_RELDISC_COOC_CAP = 40_000         # max tracked pairs (homeostatic memory bound)
_RELDISC_DECAY = 0.9               # per-cycle synaptic down-scaling
_RELDISC_DECAY_EVERY = 30.0        # seconds between homeostatic decay passes
_RELDISC_COMMON_TOKEN_CAP = 80     # tokens shared by >N concepts are non-discriminative
# (cid_a, cid_b) -> [weight, last_seen_epoch]; cid_a < cid_b
_RELDISC_COOC: dict[tuple[str, str], list[float]] = {}
_RELDISC_LINKED: set[tuple[str, str]] = set()   # pairs already emitted as relations
_RELDISC_INDEX: tuple[dict[str, set[str]], dict[str, str]] | None = None
_RELDISC_INDEX_AT: float = 0.0
_RELDISC_LAST_DECAY: float = 0.0
_RELDISC_STREAM = None             # persistent corpus sentence generator


def _reldisc_load_concepts() -> list[tuple[str, str]]:
    global _RELDISC_CONCEPTS, _RELDISC_CONCEPTS_AT
    now = _time.time()
    if _RELDISC_CONCEPTS and now - _RELDISC_CONCEPTS_AT < 45:
        return _RELDISC_CONCEPTS
    rows: list[tuple[str, str]] = []
    store = _resolve_candidate_store_path()
    if store:
        path = Path(store) / "concepts.jsonl"
        try:
            for line in path.read_text(encoding="utf-8").splitlines()[:8000]:
                line = line.strip()
                if not line:
                    continue
                try:
                    row = json.loads(line)
                except Exception:
                    continue
                cid = str(row.get("concept_id") or "")
                name = str(row.get("canonical_name") or "")
                if cid and name:
                    rows.append((cid, name))
        except Exception:
            pass
    _RELDISC_CONCEPTS = rows
    _RELDISC_CONCEPTS_AT = now
    return rows


def _reldisc_tokens(name: str) -> set[str]:
    return {t for t in re.split(r"[^0-9A-Za-z가-힣]+", name.lower()) if len(t) >= 4}


def _reldisc_is_entity_like(name: str) -> bool:
    """A relation is only meaningful between real concepts. A single all-lowercase
    common word ("through", "carrying", "finals") is too generic to be a relation
    endpoint — even when its name truly co-occurs in a sentence — so it is excluded
    from relation formation. Keep multi-word terms and anything with an uppercase
    letter (proper nouns / acronyms / Wikipedia-title concepts)."""
    n = name.strip()
    if len(n) < 3:
        return False
    if " " in n:
        return True
    return any(c.isupper() for c in n)


def _reldisc_get_index(concepts: list[tuple[str, str]]) -> tuple[dict[str, set[str]], dict[str, str]]:
    """Build (token -> {cids}) and (cid -> name) once per concept refresh.

    Ultra-common tokens (shared by many concepts) are dropped: they are not
    discriminative and would invite spurious matches. Non-entity concepts (single
    generic lowercase words) are excluded from relation formation entirely.
    """
    global _RELDISC_INDEX, _RELDISC_INDEX_AT
    now = _time.time()
    if _RELDISC_INDEX is not None and now - _RELDISC_INDEX_AT < 45:
        return _RELDISC_INDEX
    token_to_cids: dict[str, set[str]] = {}
    cid_to_name: dict[str, str] = {}
    for cid, name in concepts:
        if not _reldisc_is_entity_like(name):
            continue
        cid_to_name[cid] = name
        for tok in _reldisc_tokens(name):
            token_to_cids.setdefault(tok, set()).add(cid)
    for tok in list(token_to_cids):
        if len(token_to_cids[tok]) > _RELDISC_COMMON_TOKEN_CAP:
            del token_to_cids[tok]
    _RELDISC_INDEX = (token_to_cids, cid_to_name)
    _RELDISC_INDEX_AT = now
    return _RELDISC_INDEX


def _reldisc_sentence_stream():
    """Endless generator over real corpus sentences (the firehose source)."""
    while True:
        files = _firehose_corpus_files()
        if not files:
            yield None
            continue
        emitted = False
        for f in files:
            for sentence in _firehose_iter_sentences(f):
                emitted = True
                yield sentence
        if not emitted:
            yield None


def _reldisc_concepts_in_sentence(
    sentence: str,
    token_to_cids: dict[str, set[str]],
    cid_to_name: dict[str, str],
) -> list[str]:
    """Concepts that ACTUALLY appear in this real sentence (Hebbian co-activation).

    A concept counts only if its full canonical name occurs as a whole phrase — a
    shared token alone is not enough (that was the old noise). Punctuation is
    normalised to spaces so a mention followed by a comma/period still matches.
    Capped per sentence (sparse coding) so one sentence cannot explode the pairs.
    """
    norm = " " + re.sub(r"[^0-9a-z가-힣]+", " ", sentence.lower()).strip() + " "
    candidate: set[str] = set()
    for tok in _reldisc_tokens(sentence):
        candidate |= token_to_cids.get(tok, set())
    present: list[str] = []
    for cid in candidate:
        nl = re.sub(r"[^0-9a-z가-힣]+", " ", cid_to_name.get(cid, "").lower()).strip()
        if len(nl) >= 4 and f" {nl} " in norm:
            present.append(cid)
        if len(present) >= 12:
            break
    return present


def _relation_discovery_worker() -> None:
    global _RELDISC_STREAM, _RELDISC_LAST_DECAY
    if _RELDISC_STREAM is None:
        _RELDISC_STREAM = _reldisc_sentence_stream()
    while True:
        with _RELDISC_LOCK:
            if not _RELDISC["running"]:
                break
        concepts = _reldisc_load_concepts()
        if len(concepts) < 8:
            _time.sleep(1.0)
            continue
        token_to_cids, cid_to_name = _reldisc_get_index(concepts)
        now = _time.time()
        checks = 0
        fired: list[tuple[str, str, str]] = []
        for _ in range(200):  # batch of REAL sentences
            try:
                sentence = next(_RELDISC_STREAM)
            except StopIteration:
                break
            if not sentence:
                _time.sleep(0.5)
                break
            present = _reldisc_concepts_in_sentence(sentence, token_to_cids, cid_to_name)
            # Hebbian: every pair that co-occurred in THIS real sentence reinforces.
            for i in range(len(present)):
                for j in range(i + 1, len(present)):
                    key = (present[i], present[j]) if present[i] < present[j] else (present[j], present[i])
                    checks += 1
                    slot = _RELDISC_COOC.get(key)
                    if slot is None:
                        _RELDISC_COOC[key] = [1.0, now]
                    else:
                        slot[0] += 1.0
                        slot[1] = now
                        if slot[0] >= _RELDISC_LINK_THRESHOLD and key not in _RELDISC_LINKED:
                            _RELDISC_LINKED.add(key)
                            fired.append((cid_to_name.get(key[0], key[0]), cid_to_name.get(key[1], key[1]), sentence))

        # Homeostatic down-scaling + pruning (sleep-like): noise fades, signal persists,
        # and the cooc table stays bounded (the trillion-scale memory guarantee).
        pruned = 0
        if now - _RELDISC_LAST_DECAY >= _RELDISC_DECAY_EVERY:
            for key in list(_RELDISC_COOC):
                _RELDISC_COOC[key][0] *= _RELDISC_DECAY
                if _RELDISC_COOC[key][0] < 1.0:
                    del _RELDISC_COOC[key]
                    _RELDISC_LINKED.discard(key)
                    pruned += 1
            if len(_RELDISC_COOC) > _RELDISC_COOC_CAP:
                weakest = sorted(_RELDISC_COOC, key=lambda k: _RELDISC_COOC[k][0])[: len(_RELDISC_COOC) - _RELDISC_COOC_CAP]
                for key in weakest:
                    del _RELDISC_COOC[key]
                    _RELDISC_LINKED.discard(key)
                    pruned += 1
            _RELDISC_LAST_DECAY = now

        if fired:
            try:
                store = _resolve_candidate_store_path()
                if store:
                    log = Path(store) / "relation_discovery.jsonl"
                    with log.open("a", encoding="utf-8") as fh:
                        for la, lb, ev in fired[:8]:
                            fh.write(json.dumps({
                                "a": la, "b": lb,
                                "relation": "co_occurrence_reinforced",
                                "weight": _RELDISC_LINK_THRESHOLD,
                                "evidence": ev[:240],
                                "at": round(_time.time(), 3),
                                "verified": True,  # now evidence-backed: real co-occurrence, K-reinforced
                            }) + "\n")
            except Exception:
                pass

        now2 = _time.time()
        with _RELDISC_LOCK:
            _RELDISC["checks"] += checks
            _RELDISC["linked"] += len(fired)
            _RELDISC["pending_pairs"] = len(_RELDISC_COOC)
            _RELDISC["pruned"] += pruned
            if fired:
                _RELDISC["recent"] = ([f"{a} ↔ {b}" for a, b, _ in fired[-6:]] + list(_RELDISC["recent"]))[:6]
            dt = now2 - (_RELDISC["last_rate_at"] or now2)
            if dt >= 1.0:
                _RELDISC["checks_per_second"] = round((_RELDISC["checks"] - _RELDISC["last_checks"]) / dt, 1)
                _RELDISC["last_checks"] = _RELDISC["checks"]
                _RELDISC["last_rate_at"] = now2
        _time.sleep(0.1)


def cloud_brain_relation_discovery_start() -> dict[str, Any]:
    global _RELDISC_THREAD
    with _RELDISC_LOCK:
        if _RELDISC["running"]:
            return {"running": True, "already_running": True}
        _RELDISC.update({"running": True, "started_at": _time.time(), "last_rate_at": _time.time()})
    _RELDISC_THREAD = _threading.Thread(target=_relation_discovery_worker, name="atanor-relation-discovery", daemon=True)
    _RELDISC_THREAD.start()
    return {"running": True, "started": True}


# --- Firehose: ultra-light bulk sentence ingest (the "거대 발화그래프" raw layer) ---
# Streams sentences from local corpora at disk speed, dedups, and appends a bounded
# sample to an utterance log. This is the FAST "sentences SEEN" layer; concept /
# relation understanding is the slower downstream pipeline — never conflated.
_FIREHOSE_LOCK = _threading.Lock()
_FIREHOSE: dict[str, Any] = {
    "running": False,
    "started_at": None,
    "processed": 0,
    "unique": 0,
    "per_second": 0.0,
    "last_rate_at": 0.0,
    "last_processed": 0,
    "sources": 0,
}
_FIREHOSE_THREAD: _threading.Thread | None = None


def _firehose_corpus_files() -> list[Path]:
    files: list[Path] = []
    seed = _CLOUD_BRAIN_WORKTREE_ROOT / "packages" / "cloud_brain" / "seed_corpus"
    if seed.is_dir():
        files.extend(sorted(seed.glob("*.txt")))
    extra = os.environ.get("ATANOR_CORPORA_DIR")
    if extra and Path(extra).is_dir():
        for pattern in ("*.txt", "*.jsonl"):
            files.extend(sorted(Path(extra).glob(pattern)))
    return files


def _firehose_iter_sentences(path: Path):
    try:
        with path.open("r", encoding="utf-8", errors="ignore") as fh:
            is_jsonl = path.suffix == ".jsonl"
            for raw in fh:
                line = raw.strip()
                if not line or line.startswith("#"):
                    continue
                if is_jsonl:
                    try:
                        obj = json.loads(line)
                    except Exception:
                        continue
                    line = str(obj.get("text") or obj.get("sentence") or "").strip()
                if 8 <= len(line) <= 2000:
                    yield line
    except Exception:
        return


def _firehose_tick_rate() -> None:
    now = _time.time()
    with _FIREHOSE_LOCK:
        dt = now - (_FIREHOSE["last_rate_at"] or now)
        if dt >= 1.0:
            _FIREHOSE["per_second"] = round((_FIREHOSE["processed"] - _FIREHOSE["last_processed"]) / dt, 1)
            _FIREHOSE["last_processed"] = _FIREHOSE["processed"]
            _FIREHOSE["last_rate_at"] = now


def _firehose_worker() -> None:
    seen: set[int] = set()
    store = _resolve_candidate_store_path()
    utter_log = (Path(store) / "utterances.jsonl") if store else None
    while True:
        with _FIREHOSE_LOCK:
            if not _FIREHOSE["running"]:
                break
        files = _firehose_corpus_files()
        with _FIREHOSE_LOCK:
            _FIREHOSE["sources"] = len(files)
        if not files:
            _time.sleep(2.0)
            continue
        new_this_pass = 0
        for f in files:
            batch_new: list[str] = []
            count = 0
            for sentence in _firehose_iter_sentences(f):
                count += 1
                h = hash(sentence)
                if h not in seen:
                    # Bounded dedup window: 3M hashes cost ~150MB+ and were a major
                    # contributor to the VM OOM. A rotating cap keeps strong dedup at a
                    # fraction of the memory (clear-and-continue when full).
                    if len(seen) >= 500_000:
                        seen.clear()
                    seen.add(h)
                    new_this_pass += 1
                    if len(batch_new) < 4000:
                        batch_new.append(sentence)
                if count % 2000 == 0:
                    with _FIREHOSE_LOCK:
                        _FIREHOSE["processed"] += 2000
                    _firehose_tick_rate()
                    # Yield the GIL so the single-worker uvicorn event loop can serve
                    # requests. Without this, streaming a whole corpus file is one tight
                    # Python loop that monopolises the GIL and starves request handlers
                    # (the dashboard's concurrent burst then times out / 502s).
                    _time.sleep(0.03)
            with _FIREHOSE_LOCK:
                _FIREHOSE["processed"] += count % 2000
                _FIREHOSE["unique"] += len(batch_new)
            if utter_log and batch_new:
                try:
                    with utter_log.open("a", encoding="utf-8") as fh:
                        for sentence in batch_new[:2000]:
                            fh.write(json.dumps({"t": sentence}, ensure_ascii=False) + "\n")
                except Exception:
                    pass
            _firehose_tick_rate()
        # A full pass with no new uniques means we've exhausted the local corpora
        # (e.g. only the small seed) — idle so we don't peg CPU re-reading dupes.
        # Drop big public corpora into ATANOR_CORPORA_DIR for sustained 10k/sec.
        _time.sleep(3.0 if new_this_pass == 0 else 0.02)


def cloud_brain_firehose_start() -> dict[str, Any]:
    global _FIREHOSE_THREAD
    with _FIREHOSE_LOCK:
        if _FIREHOSE["running"]:
            return {"running": True, "already_running": True}
        _FIREHOSE.update({"running": True, "started_at": _time.time(), "last_rate_at": _time.time()})
    _FIREHOSE_THREAD = _threading.Thread(target=_firehose_worker, name="atanor-firehose", daemon=True)
    _FIREHOSE_THREAD.start()
    return {"running": True, "started": True}


def _wiki_random_sentences(host: str, n_titles: int = 3) -> list[tuple[str, str]]:
    def _get(url: str) -> Any:
        req = _urequest.Request(url, headers={"User-Agent": "ATANOR-learn/0.1 (cumulative-learning)"})
        with _urequest.urlopen(req, timeout=10) as resp:  # nosec B310 - bounded public API
            return json.loads(resp.read().decode("utf-8"))

    body = _get(f"https://{host}/w/api.php?action=query&list=random&rnnamespace=0&rnlimit={n_titles}&format=json")
    titles = [t["title"] for t in (body.get("query", {}) or {}).get("random", [])]
    out: list[tuple[str, str]] = []
    for title in titles:
        slug = _uparse.quote(title.replace(" ", "_"), safe="")
        try:
            summary = _get(f"https://{host}/api/rest_v1/page/summary/{slug}")
        except Exception:
            continue
        extract = str(summary.get("extract") or "")
        for raw in re.split(r"(?<=[.!?。])\s+|(?<=다\.)\s+", extract):
            seg = re.sub(r"\s+", " ", raw).strip()
            if 18 <= len(seg) <= 1800:
                out.append((title, seg))
    return out


_LEARN_TOPIC_IDX = 0


def _frontier_topics(n: int = 2) -> list[str]:
    """Graph-driven exploration topics — the graph's OWN concepts, cycled, so it grows
    outward from what it knows (no hardcoded topic list). Seeds from the base-brain pack
    concepts; the cumulative store's concepts extend the frontier over time."""
    global _LEARN_TOPIC_IDX
    names: list[str] = []
    try:
        from packages.base_brain.pack_loader import load_base_brain_pack

        for concept in load_base_brain_pack().semantic_graph.get("concepts", []) or []:
            labels = concept.get("labels") or {}
            label = str(labels.get("ko") or concept.get("canonical_name") or "").strip()  # Korean topic → Korean results
            if label and len(label) >= 2:
                names.append(label)
    except Exception:  # pragma: no cover - defensive
        names = []
    # Extend the frontier OUTWARD with concepts the graph has already LEARNED, so
    # exploration follows what it discovers (graph-driven), not a fixed seed list.
    try:
        seen = set(names)
        for _cid, name in _reldisc_load_concepts():
            name = str(name).strip()
            if 2 <= len(name) <= 30 and name not in seen:
                names.append(name)
                seen.add(name)
    except Exception:  # pragma: no cover - defensive
        pass
    if not names:
        return []
    topics = [names[(_LEARN_TOPIC_IDX + i) % len(names)] for i in range(n)]
    _LEARN_TOPIC_IDX = (_LEARN_TOPIC_IDX + n) % len(names)
    return topics


def _tavily_learn_sentences(topics: list[str]) -> list[tuple[str, str]]:
    """Pull clean sentences about each topic from the search API (Tavily) — the agreed
    web source, NOT Wikipedia-centric. Returns (topic, sentence) pairs for ingestion."""
    try:
        from app.services.web_search import _clean_web_snippet, has_search_api, provider_api_search
    except Exception:
        return []
    if not has_search_api():
        return []
    out: list[tuple[str, str]] = []
    for topic in topics:
        for row in provider_api_search(topic, 4):
            for raw in re.split(r"(?<=[.!?。])\s+|(?<=다\.)\s+", _clean_web_snippet(str(row.get("snippet") or ""))):
                seg = re.sub(r"\s+", " ", raw).strip()
                if 18 <= len(seg) <= 1000:
                    out.append((topic, seg))
    return out


def _run_plasticity_maintenance() -> dict[str, Any]:
    """Neuroplasticity 'sleep consolidation' over the active candidate store's relations:
    data-derived predicate informativeness weight + time decay (LTD) + pruning. Runs in the
    worker thread BETWEEN learning ticks (single-threaded -> no write race). Bounds memory:
    un-reobserved low-weight edges fade and are pruned; IS_A taxonomy is held strong inside
    plasticity_tick. See docs/neuroplasticity_live_wiring_design.md."""
    from packages.cloud_brain.streaming_plasticity import streaming_plasticity_tick
    from packages.cloud_brain.candidate_read_model import resolve_candidate_store

    ref = resolve_candidate_store()
    if not ref.path:
        return {}
    rel_path = ref.path / "relations.jsonl"
    if not rel_path.exists():
        return {}
    # STREAMING maintenance (난제 P4, LSM insight): O(1) memory per row — never load
    # the whole store on the 1GB VM again. Pruned edges are archived (reversible
    # forgetting, P2) inside the streaming pass itself.
    stats = streaming_plasticity_tick(
        rel_path,
        datetime.now(timezone.utc),
        half_life_days=float(os.getenv("ATANOR_PLASTICITY_HALFLIFE_DAYS", "60") or 60),
        prune_floor=float(os.getenv("ATANOR_PLASTICITY_FLOOR", "0.04") or 0.04),
        archive_root=ref.path,
    )
    # truth discovery (난제 P2): refresh source-trust / claim-belief scores over the
    # consensus ledger so promotion and answering can weight evidence by reliability.
    try:
        from packages.cloud_brain.consensus_ledger import ConsensusLedger
        from packages.cloud_brain.truth_discovery import score_and_persist

        ledger_root = ref.path / "consensus_ledger"
        if (ledger_root / "evidence_ledger.jsonl").exists():
            score_and_persist(ConsensusLedger(ledger_root))
    except Exception:  # scoring must never break maintenance
        pass
    return stats


def _candidate_store_concept_count(store_path: str | None) -> int:
    """Cheap line count of a candidate store's concepts.jsonl (growth signal)."""
    if not store_path:
        return 0
    try:
        p = Path(store_path) / "concepts.jsonl"
        if not p.exists():
            return 0
        with p.open("rb") as fh:
            return sum(1 for _ in fh)
    except Exception:
        return 0


def _maybe_promote_pack(force: bool = False) -> dict[str, Any] | None:
    """The learning->answers bridge. The continuous learner grows a candidate store,
    but general answers read a SEPARATE base pack — so without this, learning never
    reaches answers (the pack goes stale). Periodically re-promote the pack from the
    curated clean foundation PLUS the live store the learner actually grows, through
    the promoter's existing strict quality gates (definitional/aboutness/in-degree).

    Bounded + safe: runs only when the store GREW by a threshold (or force), writes
    the pack atomically, and never raises into the loop. Worker-thread-local."""
    try:
        live_store = _resolve_candidate_store_path()
        size = _candidate_store_concept_count(live_store)
        with _CONT_LOCK:
            last = int(_CONT.get("_last_promoted_store_size") or 0)
        min_growth = int(os.getenv("ATANOR_PROMOTE_MIN_GROWTH", "40") or 40)
        if not force and (size - last) < min_growth:
            return None
        import importlib
        promo = importlib.import_module("scripts.promote_graph_to_pack")
        extra = [Path(live_store)] if live_store else None
        result = promo.promote(dry_run=False, extra_stores=extra)
        stamp = {
            "at": _time.time(),
            "total": result.get("total"),
            "promoted_from_cloud": result.get("promoted"),
            "source_store": Path(live_store).name if live_store else None,
            "store_concepts": size,
        }
        with _CONT_LOCK:
            _CONT["promotion"] = stamp
            _CONT["_last_promoted_store_size"] = size
        return stamp
    except Exception as exc:  # pragma: no cover - never kill the loop
        with _CONT_LOCK:
            _CONT["last_error"] = f"promote: {type(exc).__name__}: {exc}"[:160]
        return None


def _continuous_worker() -> None:
    hosts = ("en.wikipedia.org", "ko.wikipedia.org")
    hi = 0
    # 20s default (was 60): the owner's fluency gap is graph SIZE, so the always-on
    # learner paces 3x faster — still one polite public fetch per tick (proper UA,
    # backoff on errors) and bounded by the same acceptance gates.
    _learn_interval = float(os.getenv("ATANOR_LEARN_INTERVAL_SEC", "20") or 20)
    _daily_cap = int(os.getenv("ATANOR_LEARN_DAILY_CAP", "800") or 800)
    _day = _time.strftime("%Y-%m-%d")
    _used_today = 0
    while True:
        with _CONT_LOCK:
            if not _CONT["running"]:
                break
            backoff = _CONT["backoff_until"]
        if _time.time() < backoff:
            _time.sleep(0.5)
            continue
        # Daily budget reset for the search API (free tier is small).
        _today = _time.strftime("%Y-%m-%d")
        if _today != _day:
            _day, _used_today = _today, 0
        # SOURCE = the search API (Tavily), graph-driven topics — the agreed design. Fall
        # back to random Wikipedia only when no key is set or the daily budget is spent,
        # so the learner keeps growing the graph either way.
        from app.services.web_search import has_search_api as _has_api

        use_api = _has_api() and _used_today < _daily_cap
        try:
            if use_api:
                topics = _frontier_topics(2)
                _used_today += len(topics)
                batch = _tavily_learn_sentences(topics)
                src_tag, lang_default, source_label = "tavily", "ko", "tavily-search-api"
            else:
                host = hosts[hi % 2]
                hi += 1
                batch = _wiki_random_sentences(host)
                src_tag, lang_default, source_label = f"wiki:{host}", ("ko" if host.startswith("ko") else "en"), host
        except Exception as exc:  # rate limit / network — back off, keep looping
            with _CONT_LOCK:
                _CONT["last_error"] = f"fetch: {type(exc).__name__}"
